def lookup(dn: int, pn: int) -> FlangeParams:
    """
    查询国标法兰参数

    Args:
        dn: 公称通径 (10–300)
        pn: 公称压力 (10, 16, 25, 40)

    Returns:
        FlangeParams 实例

    Raises:
        ValueError: 规格不在数据库中
    """
    if pn not in PN_DATA_MAP:
        raise ValueError(f"不支持的 PN={pn}，支持: {SUPPORTED_PN}")

    data_table = PN_DATA_MAP[pn]
    for row in data_table:
        if row[0] == dn:
            return _row_to_params(row, pn)

    available = [str(r[0]) for r in data_table]
    raise ValueError(f"DN={dn} 不在 PN{pn} 数据库中，可用: {', '.join(available)}")


def _row_to_params(row: tuple, pn: int) -> FlangeParams:
    """将数据行转为 FlangeParams"""
    dn, d, k, l, n, c, inner_d, f, d1 = row
    return FlangeParams(
        dn=dn,
        pn=pn,
        d=float(d),
        k=float(k),
        l=float(l),
        n=int(n),
        c=float(c),
        inner_d=float(inner_d),
        f=float(f),
        d1=float(d1),
        flange_type=FlangeType.PLATE,
        seal_type=SealType.RF,
    )


def list_available(pn: Optional[int] = None) -> list[dict]:
    """
    列出所有可用规格

    Args:
        pn: 按压力过滤（可选）

    Returns:
        dict 列表: {dn, pn, d, k, l, n, c, inner_d}
    """
    results = []
    for pn_key in SUPPORTED_PN:
        if pn is not None and pn_key != pn:
            continue
        for row in PN_DATA_MAP[pn_key]:
            dn, d, k, l, n, c, inner_d, *_ = row
            results.append({
                "dn": dn, "pn": pn_key,
                "d": d, "k": k, "l": l,
                "n": n, "c": c, "inner_d": inner_d,
            })
    return results


def is_supported(dn: int, pn: int) -> bool:
    """检查规格是否在数据库中"""
    if pn not in PN_DATA_MAP:
        return False
    return any(r[0] == dn for r in PN_DATA_MAP[pn])


