def design_and_generate(
    Q: float, P: float, n: float,
    airfoil: str = "clark_y",
    material: str = "Q235B",
    output_dir: str = ".",
    gen_macro: bool = True,
    gen_curves: bool = False,
    gen_model: bool = False,
) -> dict:
    """
    完整流程：设计 → 生成 VBA 宏（可选: 曲线/3D 模型）

    Returns:
        {
            "design": AxialFanResult,
            "summary": str,
            "macro_path": str or None,
            "curve_paths": list[str],
            "model_path": str or None,
        }
    """
    from .design import design_axial_fan
    from .params import AxialFanInput

    inp = AxialFanInput(Q=Q, P=P, n=n, airfoil=airfoil, material=material)
    design = design_axial_fan(inp)
    result = {
        "design": design,
        "summary": design.summary,
        "macro_path": None,
        "curve_paths": [],
        "model_path": None,
    }

    os.makedirs(output_dir, exist_ok=True)
    safe = f"Axial_Q{Q:.0f}_P{P:.0f}_n{n:.0f}"

    if gen_macro:
        path = os.path.join(output_dir, f"{safe}.bas")
        with open(path, "w", encoding="utf-8") as f:
            f.write(generate_vba_macro(design))
        result["macro_path"] = path
        print(f"  ✅ VBA Macro: {path}")

    if gen_curves and design.sections:
        loft = generate_blade_loft_profile(design.sections, design.airfoil)
        prefix = os.path.join(output_dir, safe)
        export_loft_sections(loft, prefix)
        result["curve_paths"].append(f"{prefix}_hub_curve.csv")
        result["curve_paths"].append(f"{prefix}_tip_curve.csv")

    if gen_model and HAS_PYWIN32:
        path = os.path.join(output_dir, f"{safe}.SLDPRT")
        build_axial_sw(design, output_path=path)
        result["model_path"] = path

    return result


