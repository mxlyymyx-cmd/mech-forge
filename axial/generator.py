def generate_vba_macro(design: AxialFanResult) -> str:
    """
    生成 SolidWorks VBA 宏

    宏包含完整的轴流叶轮建模步骤，在 SolidWorks 中：
      工具 → 宏 → 运行 → 选择此文件

    步骤：
    1. 轮毂旋转体
    2. 各截面翼型曲线（逐截面插入）
    3. 放样生成叶片
    4. 圆周阵列 Z 个叶片
    5. 可选：轴孔特征
    """
    p = design
    secs = p.sections

    if not secs:
        raise ValueError("设计结果无截面数据，无法生成宏")

    sections_data = []
    for idx, sec in enumerate(secs):
        # 生成该截面的翼型坐标
        upper, lower = generate_airfoil_coords(p.airfoil, 20, 20)

        # 合成为完整翼型截面
        airfoil_points = []
        for xu, yu in upper:
            airfoil_points.append((xu, yu))
        for xl, yl in reversed(lower):
            airfoil_points.append((xl, yl))

        sections_data.append({
            "idx": idx,
            "r": sec.r,
            "c": sec.c,
            "chi": sec.chi,
            "points": airfoil_points,
            "n_points": len(airfoil_points),
        })

    def m(v):
        """mm → m (VBA 用 meters)"""
        return f"{v / 1000.0:.6f}"

    # ── 生成各截面翼型点赋值 VBA 代码 ──
    section_assignments = []
    for sd in sections_data:
        r = sd["r"]
        c = sd["c"]
        chi = sd["chi"]
        chi_rad = math.radians(chi)
        n_pts = sd["n_points"]
        pts = sd["points"]

        assignments = []
        for i, (x_local, y_local) in enumerate(pts):
            xs = x_local * c
            ys = y_local * c
            x_sw = xs * math.cos(chi_rad)
            y_sw = r
            z_sw = xs * math.sin(chi_rad) + ys
            assignments.append(
                f"  sp{idx}({i * 3}) = {x_sw / 1000.0:.6f}: "
                f"sp{idx}({i * 3 + 1}) = {y_sw / 1000.0:.6f}: "
                f"sp{idx}({i * 3 + 2}) = {z_sw / 1000.0:.6f}"
            )

        section_assignments.append(
            f"' Section {sd['idx']}: r={sd['r']:.0f}mm  c={sd['c']:.0f}mm  chi={sd['chi']:.1f}°\n"
            f"Dim sp{sd['idx']}(0 To {n_pts * 3 - 1}) As Double\n"
            + "\n".join(assignments)
        )

    # ── 各截面曲线创建 ──
    section_splines = []
    for sd in sections_data:
        section_splines.append(
            f"  skMgr.Insert3DSketch True\n"
            f"  Dim spline{sd['idx']} As Object\n"
            f"  Set spline{sd['idx']} = skMgr.CreateSpline(sp{sd['idx']})\n"
            f"  skMgr.Insert3DSketch True"
        )

    # ── 放样选择 ──
    loft_selections = []
    for sd in sections_data:
        loft_selections.append(
            f"  boolStat = Part.Extension.SelectByID2(\"3DSketch{sd['idx'] + 1}\", "
            f"\"SKETCH\", 0, 0, 0, {loft_selections == []}, 0, Nothing, 0)"
        )

    hub_radius_m = p.r_hub / 1000.0
    hub_half_h_m = max(30, p.L * 0.3) / 1000.0  # 轮毂半长 (m)

    macro = f"""' SolidWorks VBA Macro - Axial Fan Impeller
' Generated by solidworks-parametric v0.1.0
'
' Design: Q={p.input_params.Q:.0f}m3/h  P={p.input_params.P:.0f}Pa  n={p.input_params.n:.0f}rpm
' Output: D={p.D:.0f}mm  d={p.d:.0f}mm  nu={p.nu:.3f}  Z={p.Z}
' Airfoil: {p.airfoil.value}  Circulation: {p.circulation_type.value}
'
' Instructions: SolidWorks -> Tools -> Macro -> Run
' ============================================================

Dim swApp As Object
Dim Part As Object
Dim skMgr As Object
Dim featMgr As Object
Dim boolStat As Boolean

Sub main()
    Set swApp = Application.SldWorks
    Set Part = swApp.NewDocument("", 0, 0, 0)
    swApp.Visible = True
    Set skMgr = Part.SketchManager
    Set featMgr = Part.FeatureManager

    ' ============================================================
    ' Step 1: Hub (body of revolution) — 轮毂旋转体
    ' ============================================================
    skMgr.InsertSketch True

    ' Hub profile: Z-axis as centerline
    Part.CreateLine2 0, 0, 0, {hub_radius_m:.6f}, 0, 0
    Part.CreateLine2 {hub_radius_m:.6f}, 0, 0, {hub_radius_m:.6f}, {hub_half_h_m:.6f}, 0
    Part.CreateLine2 {hub_radius_m:.6f}, {hub_half_h_m:.6f}, 0, 0, {hub_half_h_m:.6f}, 0
    Part.CreateLine2 0, {hub_half_h_m:.6f}, 0, 0, 0, 0

    ' Centerline axis
    Part.CreateLine2 0, {hub_half_h_m:.6f}, 0, 0, {-hub_half_h_m:.6f}, 0

    skMgr.InsertSketch True
    Part.ClearSelection2 True

    Part.Extension.SelectByID2 "", "SKETCHSEGMENT", 0, 0, 0, True, 0, Nothing, 0
    boolStat = featMgr.FeatureRevolve(True, False, False, False, False, 0, 0, 360#, 0, False, False, False)

    ' ============================================================
    ' Step 2: Blade sections (3D splines)
    ' ============================================================

{chr(10).join(section_assignments)}

    ' ============================================================
    ' Step 3: Create 3D splines for each section
    ' ============================================================

{chr(10).join(section_splines)}

    ' ============================================================
    ' Step 4: Loft — surface/blend between sections
    ' ============================================================
    Part.ClearSelection2 True

{chr(10).join(loft_selections)}

    ' Loft as thin feature (surface + thickness)
    ' Using InsertProtrusionBlend for loft
    Dim loftFeat As Object
    Set loftFeat = featMgr.InsertProtrusionBlend( _
        False, 0, False, True, 0, 0, 0, 0, 0, 0, 0, 0, False, False, False, 0)

    ' Add thickness if needed (thin feature)
    ' Set loftFeat = featMgr.InsertProtrusionThinBlend(...)

    ' ============================================================
    ' Step 5: Circular pattern — {p.Z} blades
    ' ============================================================
    Part.ClearSelection2 True
    boolStat = Part.Extension.SelectByID2("Blend1", "BODYFEATURE", 0, 0, 0, True, 0, Nothing, 0)
    Set patternFeat = featMgr.FeatureCircularPattern( _
        {p.Z}, 360#, False, False, False, "Blend1")

    Part.ViewZoomtofit2
    MsgBox "Axial Fan Impeller" & vbCrLf & _
           "D={p.D:.0f}mm  nu={p.nu:.2f}  Z={p.Z}  N={p.N_motor:.1f}kW" & vbCrLf & _
           "Airfoil: {p.airfoil.value}  Sections: {len(secs)}", _
           vbInformation, "solidworks-parametric"
End Sub
"""
    return macro


