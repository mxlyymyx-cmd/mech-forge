def design_and_generate(
    Q: float, P: float, n: float,
    blade_type: str = "backward",
    material: str = "Q235B",
    output_dir: str = ".",
    gen_macro: bool = True,
    gen_model: bool = False,
) -> dict:
    """
    完整流程：设计 → 生成 VBA 宏（可选: SW 3D 模型）

    Returns:
        {"design": ..., "summary": ..., "macro_path": ..., "model_path": ...}
    """
    from .design import design_impeller
    from .params import ImpellerDesignInput

    inp = ImpellerDesignInput(Q=Q, P=P, n=n, blade_type=blade_type, material=material)
    design = design_impeller(inp)
    result = {"design": design, "summary": design.summary, "macro_path": None, "model_path": None}

    os.makedirs(output_dir, exist_ok=True)
    safe = f"Impeller_Q{Q:.0f}_P{P:.0f}_n{n:.0f}"

    if gen_macro:
        path = os.path.join(output_dir, f"{safe}.bas")
        with open(path, "w", encoding="utf-8") as f:
            f.write(generate_vba_macro(design))
        result["macro_path"] = path
        print(f"  ✅ VBA Macro: {path}")

    if gen_model and HAS_PYWIN32:
        path = os.path.join(output_dir, f"{safe}.SLDPRT")
        build_impeller_sw(design, output_path=path)
        result["model_path"] = path

    return result


