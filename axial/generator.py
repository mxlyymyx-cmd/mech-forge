def _connect_sw(visible=True):
    pythoncom.CoInitialize()
    try:
        app = win32.GetActiveObject("SldWorks.Application")
    except Exception:
        app = win32.Dispatch("SldWorks.Application")
    app.Visible = visible
    return app


def build_axial_sw(
    design: AxialFanResult,
    output_path: str = "",
    visible: bool = True,
) -> str:
    """
    在 SolidWorks 中直接自动建模（Windows only）
    """
    if not HAS_PYWIN32:
        raise RuntimeError("Need pywin32 + Windows + SolidWorks")

    app = _connect_sw(visible)
    doc = app.NewDocument("", 0, 0.0, 0.0)
    part = doc

    try:
        p = design
        # Hub profile
        part.SketchManager.InsertSketch(True)
        r_hub = p.r_hub / 1000.0
        hub_len = max(0.03, p.L / 1000.0 * 0.3)
        lines = [
            (0, 0), (r_hub, 0),
            (r_hub, hub_len),
            (0, hub_len), (0, -hub_len),
        ]
        for i in range(len(lines) - 1):
            part.CreateLine2(*lines[i], 0, *lines[i + 1], 0)
        part.SketchManager.InsertSketch(True)
        part.ClearSelection2(True)
        part.Extension.SelectByID2("", "SKETCHSEGMENT", 0, 0, 0, True, 0, None, 0)
        part.FeatureManager.FeatureRevolve(True, False, False, False, False, 0, 0, 360.0, 0, False, False, False)

        # Blade sections (simplified)
        for idx, sec in enumerate(p.sections):
            part.SketchManager.Insert3DSketch(True)
            upper, lower = generate_airfoil_coords(p.airfoil, 20, 20)
            chi_rad = math.radians(sec.chi)
            pts = []
            for xu, yu in upper:
                xs = xu * sec.c
                ys = yu * sec.c
                pts.append((xs * math.cos(chi_rad) / 1000,
                            sec.r / 1000,
                            (xs * math.sin(chi_rad) + ys) / 1000))
            for xl, yl in reversed(lower):
                xs = xl * sec.c
                ys = yl * sec.c
                pts.append((xs * math.cos(chi_rad) / 1000,
                            sec.r / 1000,
                            (xs * math.sin(chi_rad) + ys) / 1000))

            # SW spline from points
            arr_size = len(pts) * 3
            arr = win32.Array(0.0, arr_size - 1)
            for i, (px, py, pz) in enumerate(pts):
                arr[i * 3] = px
                arr[i * 3 + 1] = py
                arr[i * 3 + 2] = pz
            part.SketchManager.CreateSpline(arr)
            part.SketchManager.Insert3DSketch(True)

        # Loft + pattern (practical when called from Windows)

        if output_path:
            os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
            part.SaveAs(output_path)
            return output_path
        return ""
    except Exception as e:
        raise RuntimeError(f"SW modeling failed: {e}")


